class Product {
    constructor(productId, productName, description, colour, size, price, categoryId, imageURL, brand) {

        this.productId = productId,
            this.productName = productName,
            this.description = description,
            this.colour = colour,
            this.size = size,
            this.price = price,
            this.categoryId = categoryId,
            this.imageURL = imageURL,
            this.brand = brand
    }
}